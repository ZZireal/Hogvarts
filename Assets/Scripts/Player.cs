public class Player
{
    public string bravery;
    public string cunning;
    public string intelligence;
    public string kindness;
    public string faculty;
    public string galleons;
    public string points;

    public Player() { }

    public Player(string bravery, string cunning, string intelligence, string kindness, string faculty, string galleons, string points)
    {
        this.bravery = bravery;
        this.cunning = cunning;
        this.intelligence = intelligence;
        this.kindness = kindness;
        this.faculty = faculty;
        this.galleons = galleons;
        this.points = points;
    }
}
